package net.voxelmine.blocks;

public class BlockColor {
	private float red, green, blue;
	public BlockColor(float red, float green, float blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	public float getRed() {
		return red;
	}
	public float getGreen() {
		return green;
	}
	public float getBlue() {
		return blue;
	}
}
